// دالة لعرض المنتجات
function showProducts(categoryId) {
    const container = document.getElementById('products-container');
    container.innerHTML = '';

    const products = productsData[categoryId];
    products.forEach((product, index) => {
        const productDiv = document.createElement('div');
        productDiv.classList.add('product');

        const imageGallery = product.images.map((image, idx) => {
            return `<img src="${image}" alt="صورة ${idx + 1}" class="${idx === 0 ? 'active' : ''}" />`;
        }).join('');

        productDiv.innerHTML = `
            <div class="image-gallery" data-product-id="${index}">
                ${imageGallery}
                <div class="gallery-buttons">
                    <button onclick="changeImage(${index}, -1)"> </button>
                    <button onclick="changeImage(${index}, 1)"> Next </button>
                </div>
            </div>
            <h3>${product.name}</h3>
            <div class="details">${product.details}</div>
            <div class="price">${product.price}</div>
            <button onclick="addToCart(${categoryId}, ${index})">Buy</button>
        `;

        container.appendChild(productDiv);
    });
}

// دالة لإضافة المنتج إلى السلة
function addToCart(categoryId, productIndex) {
    const product = productsData[categoryId][productIndex];
    
    // جلب السلة من localStorage أو إنشاء سلة جديدة إذا لم تكن موجودة
    let cart = JSON.parse(localStorage.getItem('cart')) || [];

    // إضافة المنتج إلى السلة
    cart.push(product);

    // حفظ السلة في localStorage
    localStorage.setItem('cart', JSON.stringify(cart));

    alert(`${product.name} تم إضافته إلى السلة!`);
}
// دالة لتغيير الصورة المعروضة
function changeImage(productIndex, direction) {
    const productDiv = document.querySelectorAll('.image-gallery')[productIndex]; // جلب الـ product div
    const images = productDiv.querySelectorAll('img'); // جلب كل الصور في المعرض
    let activeIndex = Array.from(images).findIndex(img => img.classList.contains('active')); // إيجاد الصورة النشطة (المعروضة حاليًا)

    // حساب الفهرس الجديد للصورة النشطة بناءً على الاتجاه
    let newIndex = activeIndex + direction;

    // التأكد أن الفهرس الجديد لا يخرج عن نطاق الصور
    if (newIndex < 0) newIndex = images.length - 1;
    if (newIndex >= images.length) newIndex = 0;

    // إزالة الكلاس 'active' من الصورة الحالية
    images[activeIndex].classList.remove('active');
    
    // إضافة الكلاس 'active' للصورة الجديدة
    images[newIndex].classList.add('active');
}

